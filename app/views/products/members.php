                        <h3>Contributions</h3>
                        <strong>Normal Contributions</strong> <br>
                        <p>The Participating Employer determines the Normal rate of contribution to be made by a Member and the Employer. Generally, this should as a minimum be in line with the requirements under the new NSSF Act 2013 However higher contributions of say 10% by the employee and 10% by the Participating Employer are recommended to enable a member retired on a decent pension income.</p> <br>
                        <p>For an individual joining the Mwavuli Individual Pension Plan, contributions are flexible and may be of a fixed amount. A minimum monthly contribution rate of Kshs 500 per month is applicable.</p>

                        <strong>Additional Voluntary Contribution</strong>
                        <p>A member may also opt to make Additional Voluntary Contributions above their normal contributions.</p> <br>
                        <p>Additional Voluntary Contributions are credited to a member s account and earn interest.</p> <br>
                        <p>Additional Voluntary Contributions are 100% payable as a cash lumpsum</p>

                        <strong>Investments of Contributions</strong>
                        <p>The contributions made by the member and Participating Employer are invested with our panel of fund managers/ insurers.</p> <br>
                        <p>Returns earned on the investments are credited annually after the financial statements of Mwavuli have been prepared.</p>
