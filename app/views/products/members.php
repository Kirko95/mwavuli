                            <h3>Our Benefits</h3>
                            <p>Mwavuli operates as a Defined Contribution Scheme in which benefits are based on the contributions made and returns earned on the contributions. The benefits payable are as follows:</p>
                            <strong>Normal Retirement Benefits</strong> <br>
                            Upon reaching the Normal Retirement age a member is entitled to receive his/her accumulated fund. <br>

                            The options available to the member include: <br><br>
                            <ul class="list icons list-unstyled">
                                <li><i class="icon icon-check"></i> To utilise the whole accumulated fund to buy an annuity from an insurance company of his choice. A member has upto one (1) year to select their preferred insurance company. Mwavuli assists its members to make an informed choice when selecting their preferred insurance company.</li>
                                <li><i class="icon icon-check"></i> A member may also opt to receive as a cash lumpsum upto one-third (1/3) of their accumulated fund and utilise the balance to buy an annuity.</li>
                                <li><i class="icon icon-check"></i> As an alternative to purchasing an annuity, a member may retain their accumulated fund in Mwavuli and opt to receive their benefits as a regular income out of their accumulated fund. This is termed an income drawdown arrangement.</li>
                            </ul>

                            <strong>Early Retirement Benefits</strong> <br>
                            A member may choose to retire early with the consent of their Participating Employer upto attaining the early retirement age of 50 years. In such circumstances, a member is entitled to receive the same benefits as for Normal Retirement. <br>

                            <strong>Ill Health Benefits</strong> <br>
                            <p>A member who retires on ill-health grounds as certified by a medical practitioner appointed by the trustee, is eligible to immediately receive their benefits that are payable in a similar manner as for Normal Retirement.</p>

                            <strong>Withdrawal Benefits</strong>
                            <p>On resignation, a member is entitled to receive upto 100 of their own portion of the accumulated fund and upto 50% of the Employer's portion of the accumulated fund as a cash lumpsum. The balance must be deferred to be paid on retirement. However, Protected Benefits must be fully deferred until retirement.</p> <br>
                            <p>Protected Benefits refer to the portion of accumulated fund arising from statutory contributions as required under the new NSSF Act, 2013. Please click here for more information on the new NSSF Act, 2013.</p>

                            <strong>Death Benefits</strong>
                            <p>Beneficiaries are entitled to the accumulated fund of a member upon the death of the member while still in the service of the Participating Employer.</p>

                            <strong>What is an Annuity?</strong>
                            <p>An annuity is an arrangement in which you utilise your accumulated contribution balance to purchase from an insurer a guaranteed regular income that is payable for life. When purchasing an annuity, Mwavuli assists its members to select a preferred insurer of their choice.</p>

                            <strong>What is an Income Drawdown Arrangement?</strong>
                            <p>An income draw down allows a member to retain their accumulated balance invested in Mwavuli. A member can then make regular withdrawals from their accumulated fund balance.</p>
