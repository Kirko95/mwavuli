                <h2 class="word-rotator-title">
                    Opting Out Of NSSF
                </h2>

                <div class="row">
                    <div class="col-md-12">
                        <p>
                            According to the new NSSF Act 2013 ,every employed person is required to contribute 6% of their Gross Salary to the NSSF. In addition the employer is also required to make an equivalent contribution of 6% of Gross Salary. The total contribution amount of NSSF is therefore expected to be 12% of Gross Salary.
                        </p>
                        <p>
                            NSSF contributions are to be made in two parts, Tier I contributions and Tier II contributions . Tier I contributions must be made to the NSSF whilst Tier II contributions may be made to the NSSF or to an approved pension scheme. Mwavuli is designed to be such an approved pension scheme. Click here to view the illustrative contributions under the new NSSF Act.
                        </p>
                        <p>
                            At present, sections of the new NSSF Act 2013 have been suspended by the courts and the payment of Tier I and II contributions have not yet been implemented. Contributions to the NSSF continue in line with the old provisions of Kshs 200 per month each for the employee and Employer.
                        </p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-md-12">
                        <p class="lead">
                            <strong>The NSSF Tier I</strong>
                            <p>NSSF Act 2013 stipulates that Tier I is a mandatory contribution that is to be remitted exclusively to the NSSF. It is to be based on pensionable earnings upto the Lower Earnings Limit. The Lower Earnings Limit is set as the average minimum wage.</p>
                            <p>The Lower Earnings Limit is however to be implemented gradually over a period of 5 years, starting at Kshs 6,000 in year 1, then to Kshs 7,000 then to Kshs 8,000 then to Kshs 9,000 and then to the average minimum wage by year 5 (estimated at Kshs 12,500 per month by the NSSF as at June 2014)</p>
                        </p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-md-12">
                        <p class="lead">
                            <strong>The NSSF Tier II</strong>
                            <p>NSSF Tier II contributions is also a mandatory statutory contribution but which may be paid into the NSSF or an approved pension scheme. NSSF Tier II contributions are payable in respect of gross salary that is above the Lower Earnings Limit and upto the Upper Earnings Limit of 4 times the Averange National Earnings (estimated at Kshs 144,000 per month by the NSSF as at June 2014).</p>
                            <p>The Upper Earnings Limit is also to be gradually implemented over a period of 5 years starting at 50% of the Average National Earnings to 1 times the National Average Earnings in year 2 , then to 2 times in year 3, then 3 times in year 4 and then 4 times the National Average Earnings in year 5. No Tier II contributions are payable in respect of gross salary above the Upper Earnings Limit.</p>
                        </p>
                    </div>
